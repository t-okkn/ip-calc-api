CREATE TABLE IF NOT EXISTS `Result` (
  `id` VARCHAR (36) NOT NULL PRIMARY KEY,
  `question_number` TINYINT UNSIGNED NOT NULL DEFAULT '0',
  `ip` VARCHAR (15) NOT NULL DEFAULT '0.0.0.0',
  `subnet` VARCHAR (15) NOT NULL DEFAULT '0.0.0.0',
  `answer_nw` VARCHAR (15) NOT NULL DEFAULT '0.0.0.0',
  `answer_bc` VARCHAR (15) NOT NULL DEFAULT '0.0.0.0',
  `create_datetime` DATETIME NOT NULL DEFAULT '1970-01-01 09:00:00'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
